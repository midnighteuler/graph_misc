import networkx as nx
import graphviz as gv
import tempfile
import unittest
import os

def nxtogv_draw(g):
    f = tempfile.NamedTemporaryFile(delete=False)
    f_name = f.name
    f.close()
    nx.drawing.nx_pydot.write_dot(g, f_name)
    f = open(f_name)
    d = f.read()
    f.close()
    os.unlink(f_name)
    
    gv_g = gv.Source(d)
    gv_g.render()
    
    return gv_g

